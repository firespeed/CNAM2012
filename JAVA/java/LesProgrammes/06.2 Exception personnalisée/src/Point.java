
class Point
{ 
	private int x, y ;

	public Point(int x, int y) throws ErrConst
	{ 
		// lance une exception de type ErrConst
		if ( (x<0) || (y<0)) throw new ErrConst() ; 
		this.x = x ; 
		this.y = y ; 					
	}
	
	public void affiche()
	{ 
		System.out.println ("coordonnees : " + x + " " + y) ;
	}
}
